package com.example.hardeep.something;

public class UserInfo {

    private String username;
    private String name;


    public UserInfo(String username, String name) {
        super();
        this.username = username;
        this.name = name;
    }

    public String getUsername() {
        return username;
    }

    public String getName() {
        return name;
    }
 }
